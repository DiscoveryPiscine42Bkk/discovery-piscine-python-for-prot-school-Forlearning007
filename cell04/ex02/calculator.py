Num = int(input("Give me the first number: "))
Num1 = int(input("Give me the second number: "))
print("Thank you!")
Buak = Num + Num1
print(str(Num) + " + " + str(Num1) + " = " + str(Buak))
Loap = Num - Num1
print(str(Num) + " - " + str(Num1) + " = " + str(Loap))
Koon = Num * Num1
print(str(Num) + " * " + str(Num1) + " = " + str(Koon))
Han = Num // Num1
print(str(Num) + " / " + str(Num1) + " = " + str(Han))
